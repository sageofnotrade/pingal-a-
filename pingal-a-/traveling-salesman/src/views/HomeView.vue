<div class="instructions">
  <h2>How to Embark on Your Sales Journey</h2>
  <ol>
    <li>
      <strong>Set Salesman:</strong> Select the <strong>Salesman</strong> tool <span style="vertical-align: middle; display: inline-block;"><SalesmanIcon style="width: 1.2em; height: 1.2em;" /></span> and click a cell to place your eager traveling salesman at his starting city block.
    </li>
    <li>
      <strong>Set Shop:</strong> Choose the <strong>Shop</strong> tool <span style="vertical-align: middle; display: inline-block;"><ShopIcon style="width: 1.2em; height: 1.2em;" /></span> and click a cell to set the destination store for your salesman's big sale.
    </li>
    <li>
      <strong>Add Roadblocks:</strong> Use the <strong>Roadblock</strong> tool <span style="vertical-align: middle; display: inline-block;"><TrafficConeIcon style="width: 1.2em; height: 1.2em;" /></span> to place traffic cones and block tricky city streets.
    </li>
    <li>
      <strong>Add Tolls:</strong> Select the <strong>Toll</strong> tool <span style="vertical-align: middle; display: inline-block;"><TollBoothIcon style="width: 1.2em; height: 1.2em;" /></span> to add toll booths that cost extra effort to cross.
    </li>
    <li>
      <strong>Clear Route:</strong> Use the <strong>Clear Route</strong> button to remove the current path but keep your city's layout.
    </li>
    <li>
      <strong>Reset Map:</strong> Start fresh by clicking <strong>Reset Map</strong> to clear all blocks, tolls, and points.
    </li>
    <li>
      <strong>Start Sales Journey:</strong> Hit <strong>Start Sales Journey</strong> and watch your salesman navigate the city, dodging roadblocks and paying tolls, to reach the shop and close the deal!
    </li>
  </ol>
  <p style="margin-top: 1em; color: #f39c12; font-weight: bold;">Tip: Try different city layouts and see how your salesman finds the best route!</p>
</div>

